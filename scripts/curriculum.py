def section_target_counts(total: int, proportions: dict[str, float]) -> dict[str, int]:
    """Розподіляє total запитань між розділами за пропорціями."""
    counts: dict[str, int] = {}
    remaining = total
    # Сортуємо за пропорцією за спаданням; останньому йде залишок
    sorted_sections = sorted(proportions.keys(), key=lambda k: -proportions[k])
    for i, section in enumerate(sorted_sections):
        if i == len(sorted_sections) - 1:
            counts[section] = remaining
        else:
            c = round(total * proportions[section])
            counts[section] = c
            remaining -= c
    return counts

